// Multiply
function multiply(a, b) {
  return a * b;
}

multiply(5, 2);

// Even or Odd
function evenOrOdd(number) {
  return number % 2 === 0 ? "Even" : "Odd";
}

evenOrOdd(2);

// Reversed Strings
function reverseString(str) {
  return str.split("").reverse().join("");
}

reverseString("world");
